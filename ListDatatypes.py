''' List Is Mutable.
Mutable -  We can change the existing behavior.
List is data type that allows multiple values and can be different data types.'''

values = [1, 2, "Raju", 4, 6]
print(values)
print(values[0])  #1
print(values[-1])  #6
print(values[0:2]) # 1, 2, "Raju"

